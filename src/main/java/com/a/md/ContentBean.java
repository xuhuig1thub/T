package com.a.md;

public class ContentBean {
    String body;
    String title;
    String image;
    public String getBody() {
        return body;
    }

    public String getTitle() {
        return title;
    }

    public String getImage() {
        return image;
    }

    public void setBody(String body) {
        this.body = body;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public void setImage(String image) {
        this.image = image;
    }
}
